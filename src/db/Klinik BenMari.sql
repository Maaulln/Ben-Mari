--  DDL for View V_REKAM_DAN_RESEP
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##KLINIK_ADMIN"."V_REKAM_DAN_RESEP" ("REKAM_ID", "TGL_PERIKSA", "PASIEN", "DOKTER", "DIAGNOSIS", "NAMA_OBAT", "DOSIS", "ATURAN_PAKAI", "JUMLAH", "SUBTOTAL_RESEP") AS 
  SELECT RM.REKAM_ID, RM.TGL_PERIKSA, P.NAMA_LENGKAP AS PASIEN,
    D.NAMA_DOKTER AS DOKTER, RM.DIAGNOSIS, O.NAMA_OBAT,
    R.DOSIS, R.ATURAN_PAKAI, R.JUMLAH,
    (O.HARGA_SATUAN * R.JUMLAH) AS SUBTOTAL_RESEP
FROM REKAM_MEDIS RM
JOIN APPOINTMENT A ON RM.APPOINTMENT_ID = A.APPOINTMENT_ID
JOIN PASIEN P ON A.PASIEN_ID = P.PASIEN_ID
JOIN DOKTER D ON RM.DOKTER_ID = D.DOKTER_ID
LEFT JOIN RESEP R ON RM.REKAM_ID = R.REKAM_ID
LEFT JOIN OBAT O ON R.OBAT_ID = O.OBAT_ID
;
