  CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##KLINIK_ADMIN"."V_APPOINTMENT_LENGKAP" ("APPOINTMENT_ID", "TGL_APPOINTMENT", "JAM_APPOINTMENT", "NOMOR_ANTRIAN", "PASIEN", "TELP_PASIEN", "DOKTER", "SPESIALISASI", "KELUHAN_AWAL", "STATUS") AS 
